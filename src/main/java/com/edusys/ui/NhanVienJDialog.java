    void fillTable(){
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectALl();
            for(NhanVien nv:list){
                Object[] row = {
                    nv.getMaNV(),
                    nv.getMatKhau(),
                    nv.getHoTen(),
                    nv.isVaiTro()?"Truong Phong":"NhanVien"
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void edit(){
        try {
            String MaNV = (String) tblNhanVien.getValueAt(this.row, 0);
            NhanVien model = dao.selectById(MaNV);
            if(model!=null){
                setForm(model);
                updateStatus(); 
                tabs.setSelectedIndex(0);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }
    
    void setForm(NhanVien model){
        txtMaNV.setText(model.getMaNV());
        txtHoTen.setText(model.getHoTen());
        txtMatKhau.setText(model.getMatKhau());
        txtMatKhau2.setText(model.getMatKhau());
        rdoTruongPhong.setSelected(model.isVaiTro());
        rdoNhanVien.setSelected(!model.isVaiTro());
    }
    
    NhanVien getForm(){
        NhanVien model = new NhanVien();
        model.setMaNV(txtMaNV.getText());
        model.setHoTen(txtHoTen.getText());
        model.setMatKhau(new String(txtMatKhau.getPassword()));
        model.setVaiTro(rdoTruongPhong.isSelected());
        return model;
    }
    
    void updateStatus(){
        boolean edit = this.row >=0;
        boolean first = this.row ==0;
        boolean last = this.row == tblNhanVien.getRowCount()-1;
        txtMaNV.setEditable(!edit);
        // khi insert thì không update, delete
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);
        
        btnFirst.setEnabled(edit && !first);
        btnPre.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }
    
    void clearForm(){
        this.setForm(new NhanVien());
        this.updateStatus();
        row = -1;
        updateStatus();
    }
    
    void insert(){
        NhanVien model = getForm();
        String confirm = new String (txtMatKhau2.getPassword());
        if(confirm.equals(model.getMatKhau())){
            try {
                dao.insert(model);
                this.fillTable();
                this.clearForm();
                MsgBox.alert(this, "Thêm mới thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Thêm mới thất bại!");
            }
        }else{
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng!");
        }
    }
    
    void update(){
        NhanVien model = getForm();
        String confirm = new String (txtMatKhau2.getPassword());
        if(!confirm.equals(model.getMatKhau())){
            MsgBox.alert(this, "Xác nhận mật khẩu không đúng!");
        }else{
            try {
                dao.update(model);
                this.fillTable();
                MsgBox.alert(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Cập nhật thất bại!");
            }
        }
    }
    
    void de1lete(){
        if(!Auth.isManager()){
            MsgBox.alert(this, "Bạn không có quyền xoá nhân viên!");
        }else{
            if(MsgBox.confirm(this,"Bạn thực sự muốn xoá nhân viên này?")){
                String manv = txtMaNV.getText();
                try {
                    dao.delete(manv);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alert(this, "Xoá thành công!");
                } catch (Exception e) {
                    MsgBox.alert(this, "Xoá thất bại!");
                }
            }
        }
    }
    
    void first(){
        row = 0;
        edit();
    }
    void pre(){
        if(row>0){
            row--;
            edit();
        }
    }
    void next(){
        if(row < tblNhanVien.getRowCount()-1){
            row++;
            edit();
        }
    }
    void last(){
        row = tblNhanVien.getRowCount()-1;
        edit();
    }
